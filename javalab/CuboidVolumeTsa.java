
public class CuboidVolumeTsa{
    public static void main(String[] args){
        int l=2, b=3, h=4, tsa, volume;
        tsa=2*(l*b+b*h+l*h);
        volume=l*b*h;
        System.out.println("The total surface area is "+tsa+" and volume is "+volume);
        System.out.println("Author- Sami Gautam");
    }
}
